		<h1>About TEDxPXL</h1>
		<div>
			TEDxPXL is an independently organized TED conference where speakers from around the world share cutting-edge ideas. During the breaks, attendees connect with each other through various interactive activities.
		</div>
		<div>
			TEDxPXL was founded in 2015 by five Hasselt University students and has remained a student-driven organization ever since.
		</div>
